grade = int(input('What is your Numerical Grade? '))

if grade >= 90 and grade <= 100:
    print("A")

elif grade <= 89 and grade >= 80:
    print('B')

elif grade <= 79 and grade >= 70:
        print('C')


elif grade <= 69 and grade >= 65:
    print('D')


elif grade <= 65:
    print('Fail')


